package com.talent.forex.bean.domain;

/**
 * AbstractRate entity provides the base persistence definition of the Rate
 * entity. @author MyEclipse Persistence Tools
 */

public abstract class AbstractRate implements java.io.Serializable {

	// Fields

	private Integer id;
	private String accType;
	private String sourceCcy;
	private String targetCcy;
	private String direction;
	private String rate;

	// Constructors

	/** default constructor */
	public AbstractRate() {
	}

	/** full constructor */
	public AbstractRate(String accType, String sourceCcy, String targetCcy,
			String direction, String rate) {
		this.accType = accType;
		this.sourceCcy = sourceCcy;
		this.targetCcy = targetCcy;
		this.direction = direction;
		this.rate = rate;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getAccType() {
		return this.accType;
	}

	public void setAccType(String accType) {
		this.accType = accType;
	}

	public String getSourceCcy() {
		return this.sourceCcy;
	}

	public void setSourceCcy(String sourceCcy) {
		this.sourceCcy = sourceCcy;
	}

	public String getTargetCcy() {
		return this.targetCcy;
	}

	public void setTargetCcy(String targetCcy) {
		this.targetCcy = targetCcy;
	}

	public String getDirection() {
		return this.direction;
	}

	public void setDirection(String direction) {
		this.direction = direction;
	}

	public String getRate() {
		return this.rate;
	}

	public void setRate(String rate) {
		this.rate = rate;
	}

}