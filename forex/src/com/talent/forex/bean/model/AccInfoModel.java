package com.talent.forex.bean.model;

public class AccInfoModel {

	private String userNum;
	private String accNo;
	private String ccy;
	private String originalAmt;
	private String amount;
	
	public AccInfoModel() {
		super();
	}

	public AccInfoModel(String userNum, String accNo, String ccy,
			String originalAmt, String amount) {
		super();
		this.userNum = userNum;
		this.accNo = accNo;
		this.ccy = ccy;
		this.originalAmt = originalAmt;
		this.amount = amount;
	}

	public String getUserNum() {
		return userNum;
	}

	public void setUserNum(String userNum) {
		this.userNum = userNum;
	}

	public String getAccNo() {
		return accNo;
	}

	public void setAccNo(String accNo) {
		this.accNo = accNo;
	}

	public String getCcy() {
		return ccy;
	}

	public void setCcy(String ccy) {
		this.ccy = ccy;
	}

	public String getOriginalAmt() {
		return originalAmt;
	}

	public void setOriginalAmt(String originalAmt) {
		this.originalAmt = originalAmt;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}
	
}
