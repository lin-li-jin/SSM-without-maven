package com.talent.forex.bean.model;

/**
 * 这个model用来接收前端的参数
 * 在增加一条即期保证金交易的时候把页面传回来的参数封装成一个model
 * Created by 吴樟 on www.haixiangzhene.xyz
 * 2017/8/25.
 */
public class MarginSpotInfoModel {

    private String accAmount;   //保证金金额
    private String dealAmt;     //扩大后的金额，单位与保证金相同
    private String price;       //当前的价格
    private String weCcy;       //卖的货币
    private String anaCcy;      //买的货币
    private String direction;   //暂时只有买的，但为了方便以后需求



    public String getAccAmount() {
        return accAmount;
    }

    public void setAccAmount(String accAmount) {
        this.accAmount = accAmount;
    }

    public String getDealAmt() {
        return dealAmt;
    }

    public void setDealAmt(String dealAmt) {
        this.dealAmt = dealAmt;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getWeCcy() {
        return weCcy;
    }

    public void setWeCcy(String weCcy) {
        this.weCcy = weCcy;
    }

    public String getAnaCcy() {
        return anaCcy;
    }

    public void setAnaCcy(String anaCcy) {
        this.anaCcy = anaCcy;
    }

    public String getDirection() {
        return direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }
}
