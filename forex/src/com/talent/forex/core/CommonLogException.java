package com.talent.forex.core;

public class CommonLogException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4409624001953244306L;

	/**
	 * 
	 */

	public CommonLogException() {
	}

	public CommonLogException(String message) {
		super(message);
	}

	public CommonLogException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommonLogException(Throwable cause) {
		super(cause);
	}
}
